#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	a;
	int	i;

	i = 0;
	a = 0;
	if (!s)
	{
		a += write(1, "(null)", 6);
		return (a);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		a++;
	}
	return (a);
}
